package Chapther3.Exercise;

public class _5_exercise {
    public static void main(String[] args) {
        short a=3;  //00000011
        short b=5;  //00000101
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a^b);
        System.out.println(~a&b);
        System.out.println(a<<1);
        System.out.println(a<<2);
        System.out.println(a>>1);
    }
}
